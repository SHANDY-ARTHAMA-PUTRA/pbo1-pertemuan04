public class Switchcase {
  public static void main(String[] args) {
    int menu=2;

    switch (menu){
        case 1:
            System.out.println("Air Mineral");
            break;
        case 2:
            System.out.println("Air Reverse Osmosis");
            break;
        default:
            System.out.println("Pilihan tidak tersedia");
      }
   }
}
    